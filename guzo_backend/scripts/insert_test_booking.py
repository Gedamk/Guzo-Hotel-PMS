# PDF GENERATOR – Heritage & Hospitality Design
# ======================================================
def generate_pdf_confirmation(hotel, guest, email, phone, checkin, checkout, nights, room, revenue):
    """Generate a Guzo-branded bilingual PDF confirmation slip."""
    logo_path = os.path.join(os.getcwd(), "assets", "guzo_logo.png")
    tmp_file = tempfile.NamedTemporaryFile(delete=False, suffix=".pdf")
    c = canvas.Canvas(tmp_file.name, pagesize=A4)
    width, height = A4

    # --- Brand Palette ---
    GUZO_BLUE = colors.HexColor("#004B8D")
    GUZO_GOLD = colors.HexColor("#FFB703")
    GUZO_GREEN = colors.HexColor("#007A33")
    GUZO_BROWN = colors.HexColor("#5C4033")
    IVORY = colors.HexColor("#FAF9F6")

    # Background
    c.setFillColor(IVORY)
    c.rect(0, 0, width, height, fill=1, stroke=0)

    # Header bar
    c.setFillColor(GUZO_BLUE)
    c.rect(0, height - 85, width, 85, fill=1, stroke=0)
    c.setFillColor(GUZO_GOLD)
    c.rect(0, height - 87, width, 3, fill=1, stroke=0)

    # Logo
    if os.path.exists(logo_path):
        c.drawImage(logo_path, 30, height - 75, width=90, height=50, mask='auto')

    # Title
    c.setFillColor(colors.white)
    c.setFont("Helvetica-Bold", 18)
    c.drawString(140, height - 50, f"{hotel} Booking Confirmation")
    c.setFont("Helvetica", 10)
    c.drawString(140, height - 65, f"Generated by Guzo Guest Assist – {datetime.now().strftime('%Y-%m-%d %H:%M')}")

    # --- Guest Information ---
    c.setFillColor(colors.black)
    c.setFont("Helvetica-Bold", 12)
    c.drawString(40, height - 120, "Guest Information:")
    c.setFont("Helvetica", 11)
    c.drawString(60, height - 140, f"Name: {guest}")
    c.drawString(60, height - 155, f"Email: {email}")
    c.drawString(60, height - 170, f"Phone: {phone}")

    # --- Booking Details ---
    c.setFont("Helvetica-Bold", 12)
    c.drawString(40, height - 200, "Booking Details:")
    c.setFont("Helvetica", 11)
    c.drawString(60, height - 220, f"Check-in: {checkin}")
    c.drawString(60, height - 235, f"Check-out: {checkout}")
    c.drawString(60, height - 250, f"Nights: {nights}")
    c.drawString(60, height - 265, f"Room Type: {room}")
    c.setFont("Helvetica-Bold", 12)
    c.setFillColor(GUZO_BLUE)
    c.drawString(60, height - 280, f"Total: {revenue:,.2f} ETB")

    # Divider
    c.setStrokeColor(GUZO_GOLD)
    c.setLineWidth(1)
    c.line(40, height - 295, width - 40, height - 295)

    # --- Footer bilingual message ---
    c.setFont("Helvetica-Oblique", 10)
    c.setFillColor(GUZO_BROWN)
    c.drawString(40, 80, "Thank you for booking with Guzo Guest Assist.")
    c.setFillColor(GUZO_GREEN)
    c.drawString(40, 65, "ጉዞዎን እንረዳለን። — Your Journey, Our Assist.")

    # Watermark
    c.saveState()
    c.setFont("Helvetica-BoldOblique", 38)
    c.setFillColorRGB(0.92, 0.92, 0.92)
    c.rotate(45)
    c.drawCentredString(420, 70, "Guzo Guest Assist Verified")
    c.restoreState()

    # Footer Details
    c.setFont("Helvetica", 8)
    c.setFillColor(colors.grey)
    c.drawString(40, 40, f"© {datetime.now().year} Guzo Guest Assist • Addis Ababa, Ethiopia • Hospitality Technology for Africa")

    c.showPage()
    c.save()
    return tmp_file.name


