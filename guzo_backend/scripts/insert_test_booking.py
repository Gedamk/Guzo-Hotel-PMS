# GOOGLE SHEETS INSERT
# ======================================================
def insert_booking(hotel, guest, nights, checkin, checkout, room, email, phone, source, rate, remark):
    try:
        scope = [
            "https://www.googleapis.com/auth/spreadsheets",
            "https://www.googleapis.com/auth/drive"
        ]
        creds_path = os.getenv("GOOGLE_APPLICATION_CREDENTIALS")
        if not creds_path or not os.path.exists(creds_path):
            raise FileNotFoundError("Service account credentials missing.")

        creds = ServiceAccountCredentials.from_json_keyfile_name(creds_path, scope)
        client = gspread.authorize(creds)

        sheet_id = os.getenv("SOFI_HOTEL_SHEET_ID")
        if not sheet_id:
            raise ValueError("SOFI_HOTEL_SHEET_ID missing in .env")

        sheet = client.open_by_key(sheet_id).sheet1
        nights = int(nights)
        rate = float(rate)
        revenue = nights * rate

        row = [
            datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            guest, source, checkin, nights, room, rate, revenue,
            "Confirmed", "TEST-" + datetime.now().strftime("%H%M%S"),
            remark, "✅ Sent", "GuzoBot"
        ]
        sheet.append_row(row, value_input_option="USER_ENTERED")
        print(f"Booking inserted successfully for {hotel}: {guest}")
        return True, revenue
    except Exception as e:
        print("Error inserting booking:", e)
        return False, 0


