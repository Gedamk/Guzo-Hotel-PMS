if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Insert booking and send bilingual, branded confirmation.")
    parser.add_argument("--hotel", required=True)
    parser.add_argument("--guest", required=True)
    parser.add_argument("--nights", required=True)
    parser.add_argument("--checkin", required=True)
    parser.add_argument("--checkout", required=True)
    parser.add_argument("--room", required=True)
    parser.add_argument("--email", required=True)
    parser.add_argument("--phone", required=True)
    parser.add_argument("--source", default="Direct")
    parser.add_argument("--rate", default=0)
    parser.add_argument("--remark", default="System test booking")
    args = parser.parse_args()

    ok, revenue = insert_booking(
        args.hotel, args.guest, args.nights, args.checkin, args.checkout,
        args.room, args.email, args.phone, args.source, args.rate, args.remark
    )

    if ok:
        send_booking_emails(
            args.hotel, args.guest, args.email, args.phone, args.checkin,
            args.checkout, args.nights, args.room, revenue, args.remark
        )

    print("\nProcess complete.\n")
