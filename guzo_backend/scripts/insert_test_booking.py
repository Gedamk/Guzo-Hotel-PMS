# EMAIL SENDER (Bilingual + Branded HTML)
# ======================================================
def send_booking_emails(hotel, guest, email, phone, checkin, checkout, nights, room, revenue, remark):
    try:
        api_key = os.getenv("SENDGRID_API_KEY")
        sender = os.getenv("SENDER_EMAIL")
        alert_recipients = os.getenv("BOOKING_ALERT_EMAIL", "")
        hotel_recipients = os.getenv("HOTEL_NOTIFICATIONS_EMAIL", "")
        all_alerts = [addr.strip() for addr in (alert_recipients + "," + hotel_recipients).split(",") if addr.strip()]

        if not api_key or not sender:
            raise ValueError("Missing SendGrid configuration in .env")

        sg = SendGridAPIClient(api_key)

        pdf_path = generate_pdf_confirmation(hotel, guest, email, phone, checkin, checkout, nights, room, revenue)
        with open(pdf_path, "rb") as f:
            encoded = base64.b64encode(f.read()).decode()

        attachment = Attachment(
            FileContent(encoded),
            FileName(f"{hotel}_Booking_{guest.replace(' ', '_')}.pdf"),
            FileType("application/pdf"),
            Disposition("attachment")
        )

        # --- Guest Email (bilingual + cultural) ---
        guest_html = f"""
        <html><body style="font-family: Arial, sans-serif; background-color:#FAF9F6; padding:20px;">
        <div style="max-width:650px; margin:auto; background:white; border-radius:10px; box-shadow:0 0 5px #ddd;">
        <div style="background-color:#004B8D; color:white; padding:20px; border-top-left-radius:10px; border-top-right-radius:10px;">
            <h2 style="margin:0;">{hotel} Booking Confirmation</h2>
        </div>
        <div style="padding:20px; color:#333;">
            <p>Dear {guest},</p>
            <p>Thank you for booking with <strong>{hotel}</strong> via <strong style='color:#004B8D;'>Guzo Guest Assist</strong>.</p>
            <p><b>Check-in:</b> {checkin}<br>
            <b>Check-out:</b> {checkout}<br>
            <b>Room:</b> {room}<br>
            <b>Total:</b> <span style="color:#004B8D;"><b>{revenue:,.2f} ETB</b></span></p>
            <hr style="border:1px solid #FFB703;">
            <h3 style="color:#004B8D;">እንኳን ደህና መጡ፣ {guest}!</h3>
            <p>መዝገብዎ ተሳክቶ ተመዝግቧል። እንኳን በደህና መጡ ወደ {hotel}።</p>
            <p style="color:#007A33;"><em>ጉዞዎን እንረዳለን። — Your Journey, Our Assist.</em></p>
            <p style="font-size:12px; color:#999;">This message was generated automatically by Guzo Guest Assist.</p>
        </div></div></body></html>
        """
        guest_msg = Mail(from_email=sender, to_emails=email,
                         subject=f"{hotel} Booking Confirmation – Guzo Guest Assist",
                         html_content=guest_html)
        guest_msg.attachment = attachment
        sg.send(guest_msg)
        print(f"Confirmation email with PDF sent to guest: {email}")

        # --- Notifications to Hotel & Internal ---
        alert_html = f"""
        <h3 style="color:#004B8D;">New Booking – {hotel}</h3>
        <p><b>Guest:</b> {guest}<br>
        <b>Email:</b> {email}<br>
        <b>Phone:</b> {phone}<br>
        <b>Check-in:</b> {checkin}<br>
        <b>Check-out:</b> {checkout}<br>
        <b>Room:</b> {room}<br>
        <b>Revenue:</b> {revenue:,.2f} ETB<br>
        <b>Remark:</b> {remark}</p>
        <p style="color:#007A33;"><em>ጉዞዎን እንረዳለን። — Your Journey, Our Assist.</em></p>
        <p style="font-size:12px; color:#777;">Logged automatically by Guzo Guest Assist.</p>
        """

        for recipient in all_alerts:
            msg = Mail(from_email=sender, to_emails=recipient,
                       subject=f"New Booking – {hotel}", html_content=alert_html)
            msg.attachment = attachment
            sg.send(msg)
            print(f"Notification email with PDF sent to: {recipient}")

        return True
    except Exception as e:
        print("Email sending error:", e)
        return False


